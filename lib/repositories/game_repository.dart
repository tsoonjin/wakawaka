import 'dart:convert';
import 'dart:io';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:http/http.dart' as http;
import 'package:waka/repositories/api_error.dart';

class HttpGameRepository {
  HttpGameRepository({required this.client});
  final http.Client client;

  Future<T> _getData<T>({
    required Uri uri,
    required T Function(dynamic data) builder,
  }) async {
    try {
      final response = await client.get(uri);
      switch (response.statusCode) {
        case 200:
          final data = json.decode(response.body);
          return builder(data);
        case 401:
          throw const APIError.invalidApiKey();
        case 404:
          throw const APIError.notFound();
        default:
          throw const APIError.unknown();
      }
    } on SocketException catch (_) {
      throw const APIError.noInternetConnection();
    }
  }
}

/// Providers used by rest of the app
final weatherRepositoryProvider = Provider<HttpGameRepository>((ref) {
  return HttpGameRepository(
    client: http.Client(),
  );
});
